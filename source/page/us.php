				    <h2 class="h1">Historique</h2>
				  
   					 <p class="blo_his mar_top_30 pad_20 tex_jus"> <img src="source/img/staff/team_111.jpg"  alt="Samuel KAMDEM, (Té Waffo Buguong), FACOP"/> Le 19 octobre 2015, mon meilleur coach dans le domaine de l'entrepreneuriat mourrait à Yaoundé, des suites de maladie, pendant que j'accompagnais quelques futurs jeunes entrepreneurs Africains à la grande foire de Canton en Chine. Il s'appelait Samuel KAMDEM, (Té Waffo Buguong), Il est mort à l'âge de 82 ans, très riche et heureux, mais aussi très fier de transmettre à son homonyme que je suis quelques secrets pour rester longtemps riche et heureux comme lui, ou mieux encore. 
						  <br/>Té Waffo n'était pas allé à l'école (système scolaire occidental), mais s'était retrouvé très tôt dans un cercle d'autoformation, constitué de commerçants très futés, couvrant plusieurs pays dont le Tchad et le Soudan, en plus de leur pays natal, le Cameroun. Il a fini industriel dans l'agroalimentaire et entrepreneur dans d'autres multiples activités. Il m'a prouvé que devenir financièrement Indépendant ne s'apprend pas à l'école. 
						 <br/>Il m'a expliqué qu'il faut simplement être bien accompagné et connaître quelques secrets, pour atteindre la sécurité financière J'ai décidé, à sa mort, de me donner comme principale mission de former et de coacher autant d'africains que possible, afin de les accompagner dans la création et le développement rapide de leurs entreprises, d'où la création d'une Société par Actions Simplifiée Future Africa Coaching Compagny (FACOCOM SAS) qui porte le projet dénommé Future Africa Coaching Program (FACOP.)....</p>
				  
				  
			  </div>
			  <div class="flo_lef wid_40">
				  
				    <h2 class="h1">Objectifs</h2>
				  
   					 <p class="mar_top_30 tex_jus"> Les objectifs de la formation FUTURE AFRICA COACHING PROGRAM (FACOP)
<br/>* Initier plus d' un millier d'Africains à une bonne maitrise de l'intelligence Financière, de l'intelligence Emotionnelle et de de l'intelligence Spirituelle.
Aider les participants à prendre conscience de leur réel potentiel et surtout à prendre la décision de changer de paradigme, de changer de camp, de changer de vie, pour réussir dans la création et le développement de leurs entreprises, à transmettre aux générations futures.
<br/>* Constituer un vaste réseau multidisciplinaire comprenant des membres compétents et intègres (Coachs, chefs d'entreprises, Notaires, Avocats, Experts Comptables, Architectes, Agents Immobiliers, etc.), pouvant orienter les participants et les aider à éviter les erreurs et les pièges du système.
<br/>* Avoir un groupe d'entrepreneurs financièrement Indépendants et capables de :
<br/>- participer à des voyages d'affaires et d'acquisition de connaissances pratiques partout dans le monde.
<br/>- devenir des coachs outillés pour former d'autres Africains à l'entreprenariat et au développement personnel.
<br/>- constituer une mutuelle Financière dans le respect des lois et règlements en vigueur, au bénéfice de ses membres.</p>
				
